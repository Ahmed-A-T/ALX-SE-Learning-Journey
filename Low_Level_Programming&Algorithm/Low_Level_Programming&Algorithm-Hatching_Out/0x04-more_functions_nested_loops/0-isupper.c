#include <stdio.h>

/**
 * _isupper - checks for uppercase character
 * @c: lower or upper case characters that entered by user to check
 *
 * Return: 1 if c is uppercase, otherwise return 0.
 */
int _isupper(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);

	return (1);
}
